package lesson03;

public class Task02 {
    public static void main(String[] args) {
        int dayOfWeek = 1;
        if (dayOfWeek == 1) {
            System.out.println("Понедельник");
        } else if (dayOfWeek == 2) {
            System.out.println("Вторник");
        } else if (dayOfWeek == 3) {
            System.out.println("Среда");
        } else if (dayOfWeek == 4) {
            System.out.println("Четверг");
        } else if (dayOfWeek == 5) {
            System.out.println("Пятница");
        } else if (dayOfWeek == 6) {
            System.out.println("Суббота");
        } else if (dayOfWeek == 7) {
            System.out.println("Воскресенье");
        } else {
            System.out.println("Номер дня недели указан не верно");
        }
    }
}
