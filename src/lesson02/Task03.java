public class Task03 {
    public static void main(String[] args) {
        float circleRadius = 5.73f;
        final float CONST_PI = 3.14f;
        System.out.println("Длина окружности равна: " + 2 * CONST_PI * circleRadius);
        System.out.println("Площадь круга равна: " + CONST_PI * circleRadius * circleRadius);
    }
}
